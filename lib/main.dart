void main() {
  runApp(MyApp());
}

class MyApp extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'Expenses Calculator',
      theme: ThemeData(
        //theme is used to set a general settings to app
        primarySwatch: Colors.purple,
